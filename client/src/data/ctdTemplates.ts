export interface CTDTemplate {
  code: string;
  title: string;
  regulatoryRef: string;
  content: string;
}

// ── Helper to build a simple HTML table ──
function table(headers: string[], rows: string[][]): string {
  const th = headers.map((h) => `<th style="background:#1A5C9E;color:#fff;padding:8px 12px;text-align:left;font-size:13px;border:1px solid #D6E4FF">${h}</th>`).join('');
  const trs = rows
    .map((row) =>
      `<tr>${row.map((cell) => `<td style="padding:7px 12px;border:1px solid #E8E8E8;font-size:13px;vertical-align:top">${cell}</td>`).join('')}</tr>`
    )
    .join('');
  return `<table style="width:100%;border-collapse:collapse;margin-bottom:16px"><thead><tr>${th}</tr></thead><tbody>${trs}</tbody></table>`;
}

function note(text: string): string {
  return `<div style="padding:10px 14px;background:#FFFBE6;border:1px solid #FFE58F;border-radius:4px;margin-bottom:12px;font-size:13px;color:#8C6E00">${text}</div>`;
}

function section(title: string): string {
  return `<div style="font-weight:700;font-size:14px;color:#1A5C9E;margin:16px 0 8px;padding-left:10px;border-left:3px solid #1A5C9E">${title}</div>`;
}

function subsection(title: string): string {
  return `<div style="font-weight:600;font-size:13px;color:#333;margin:10px 0 6px">${title}</div>`;
}

function field(label: string, placeholder = '___________'): string {
  return `<div style="display:flex;margin-bottom:6px;font-size:13px"><span style="min-width:180px;font-weight:500;color:#555">${label}</span><span style="color:#999">${placeholder}</span></div>`;
}

function tree(items: string[]): string {
  return items
    .map((item, i, arr) => {
      const isLast = i === arr.length - 1;
      const prefix = isLast ? '└──' : '├──';
      return `<div style="font-family:monospace;font-size:13px;padding:2px 0;color:#333">${prefix} ${item}</div>`;
    })
    .join('');
}

